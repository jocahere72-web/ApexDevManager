  select b.id_dscnto_rgla,
		a.cdgo_dscnto_cndcion_tpo,
		decode (cdgo_dscnto_cndcion_tpo, 'DCM', 'Documento', 'PYS', 'Paz y Salvo', 'FNC', 'Función') dscrp_cdgo_dscnto_cndcion_tpo,
		a.id_dscnto_cndcion,
		b.cdgo_clnte,
		b.nmbre_clnte,
		b.dscrpcion,
		b.id_impsto,
		b.nmbre_impsto,
		b.id_impsto_sbmpsto,
		b.nmbre_impsto_sbmpsto,
		b.vgncia_dsde,
		b.id_prdo_dsde,
		b.vgncia_hsta,
		b.id_prdo_hsta,
		b.prdo_dsde,
		b.prdo_hsta,
