
  CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_MC_G_EMBARGOS_SJTO" ("ID_EMBRGOS_CRTRA", "ID_EMBRGOS_SJTO", "ID_SJTO", "CDGO_CLNTE", "IDNTFCCION_SJTO", "IDNTFCCION_ANTRIOR", "ID_PAIS", "NMBRE_PAIS", "ID_MNCPIO", "NMBRE_MNCPIO", "ID_DPRTMNTO", "NMBRE_DPRTMNTO", "DRCCION") AS
  select A.ID_EMBRGOS_CRTRA,
        A.ID_EMBRGOS_SJTO,
        A.ID_SJTO,
        B.CDGO_CLNTE,
        B.IDNTFCCION_SJTO,
        B.IDNTFCCION_ANTRIOR,
        B.ID_PAIS,
        B.NMBRE_PAIS,
        B.ID_MNCPIO,
        B.NMBRE_MNCPIO,
        B.ID_DPRTMNTO,
        B.NMBRE_DPRTMNTO,
        B.DRCCION
   from MC_G_EMBARGOS_SJTO a
   join v_si_c_sujetos b on B.ID_SJTO = A.ID_SJTO;

