
  CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_MC_G_DESEMBARGOS_SOPORTE" ("ID_DSMBRGO_SPRTE", "ID_DSMBRGOS_RSLCION", "ID_CSLES_DSMBRGO", "DSCRPCION_CSAL", "NMRO_DCMNTO", "FCHA_DCMNTO", "NMRO_OFCIO", "VLOR_DCMNTO") AS
  select a.ID_DSMBRGO_SPRTE,
        a.ID_DSMBRGOS_RSLCION,
        A.ID_CSLES_DSMBRGO,
        B.DSCRPCION_CSAL,
        A.NMRO_DCMNTO,
        A.FCHA_DCMNTO,
        A.NMRO_OFCIO,
        A.VLOR_DCMNTO
from MC_G_DESEMBARGOS_SOPORTE a
join MC_D_CAUSALES_DESEMBARGO b on A.ID_CSLES_DSMBRGO = B.ID_CSLES_DSMBRGO;

