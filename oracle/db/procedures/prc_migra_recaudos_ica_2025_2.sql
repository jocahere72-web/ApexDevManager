
  CREATE OR REPLACE EDITIONABLE PROCEDURE "PRC_MIGRA_RECAUDOS_ICA_2025_2" (P_CDGO_CLNTE IN NUMBER,
                                                          O_MNSJE      OUT VARCHAR2) IS

  CURSOR C1 IS
    SELECT A.VGNCIA VGNCIA
      FROM MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
     WHERE A.ID_PRDO IS NULL
     GROUP BY A.VGNCIA
     ORDER BY A.VGNCIA;

  CURSOR C2 IS
    SELECT A.CDGO_CNCPTO CDGO_CNCPTO
      FROM MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
     WHERE A.ID_CNCPTO IS NULL
     GROUP BY A.CDGO_CNCPTO
     ORDER BY A.CDGO_CNCPTO;

  CURSOR C2_2 IS
    SELECT A.CDGO_CNCPTO CDGO_CNCPTO
      FROM MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
     WHERE A.ID_CNCPTO IS NULL
       AND A.CDGO_CNCPTO = '5000'
     GROUP BY A.CDGO_CNCPTO
     ORDER BY A.CDGO_CNCPTO;

  CURSOR C3 IS
    SELECT VGNCIA, ID_PRDO, ID_CNCPTO, TPO_CNCPTO
      FROM MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
     WHERE A.TPO_CNCPTO = 'I'
       AND A.ID_CNCPTO_CPTAL IS NULL
     GROUP BY VGNCIA, ID_PRDO, ID_CNCPTO, TPO_CNCPTO
     ORDER BY VGNCIA, ID_PRDO, ID_CNCPTO, TPO_CNCPTO;

  CURSOR C4 IS
    SELECT T.CDGO_BNCO, T.NMRO_CNTA
      FROM MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO T
     WHERE T.ID_BNCO IS NULL
        OR T.ID_BNCO_CNTA IS NULL
     GROUP BY T.CDGO_BNCO, T.NMRO_CNTA
     ORDER BY T.CDGO_BNCO, T.NMRO_CNTA;

  CURSOR C5 IS
    SELECT T.ID_INTRMDIA, T.ID_SJTO_IMPSTO, T.NMRO_CNVNIO
      FROM MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO T
     WHERE T.ID_CNVNIO IS NULL
       AND T.NMRO_CNVNIO IS NOT NULL
     ORDER BY T.ID_INTRMDIA;

  V_CDGO_CLNTE        DF_S_CLIENTES.CDGO_CLNTE%TYPE;
  V_ID_IMPSTO         DF_C_IMPUESTOS.ID_IMPSTO%TYPE;
  V_ID_IMPSTO_SBMPSTO DF_I_IMPUESTOS_SUBIMPUESTO.ID_IMPSTO_SBMPSTO%TYPE;
  V_ID_IMPSTO_ACTO    DF_I_IMPUESTOS_ACTO.ID_IMPSTO_ACTO%TYPE;

  V_ARCHVO       VARCHAR2(200);
  V_ARCHVO_ERROR VARCHAR2(200);
  V_FCHA_HRA     VARCHAR2(50);

  V_ID_PRDO             DF_I_PERIODOS.ID_PRDO%TYPE;
  V_ID_CNCPTO           DF_I_CONCEPTOS.ID_CNCPTO%TYPE;
  V_ID_CNCPTO_SLDO_FVOR DF_I_CONCEPTOS.ID_CNCPTO%TYPE;
  V_ID_CNCPTO_CPTAL     DF_I_CONCEPTOS.ID_CNCPTO%TYPE;
  V_CTGRIA_CNCPTO       DF_I_CONCEPTOS.CTGRIA_CNCPTO%TYPE;
  V_ID_CNVNIO           GF_G_CONVENIOS.ID_CNVNIO%TYPE;

  V_ID_BNCO      DF_C_BANCOS.ID_BNCO%TYPE;
  V_ID_BNCO_CNTA DF_C_BANCOS_CUENTA.ID_BNCO_CNTA%TYPE;

BEGIN
  V_FCHA_HRA := TO_CHAR(SYSDATE, 'yyyymmdd_hh24miss');

  V_ARCHVO       := 'PRC_MIGRA_RECAUDOS_2024_2_' || V_FCHA_HRA ||
                    '_log.txt';
  V_ARCHVO_ERROR := 'PRC_MIGRA_RECAUDOS_2024_2_' || V_FCHA_HRA ||
                    '_error.txt';

  SITPR001('Entrando a PRC_MIGRA_RECAUDOS_2024_2', V_ARCHVO);

  V_CDGO_CLNTE := NULL;

  BEGIN
    SELECT A.CDGO_CLNTE
      INTO V_CDGO_CLNTE
      FROM DF_S_CLIENTES A
     WHERE A.CDGO_CLNTE = P_CDGO_CLNTE;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      SITPR001('Error (1) en clientes: ' || SQLCODE || '-' || SQLERRM,
               V_ARCHVO_ERROR);
    WHEN OTHERS THEN
      SITPR001('Error (2) en clientes: ' || SQLCODE || '-' || SQLERRM,
               V_ARCHVO_ERROR);
  END;

  IF V_CDGO_CLNTE IS NULL THEN

    O_MNSJE := 'Error en cliente, no existe en el sistema!';
    RETURN;
  END IF;

  BEGIN
    SELECT A.ID_IMPSTO, B.ID_IMPSTO_SBMPSTO
      INTO V_ID_IMPSTO, V_ID_IMPSTO_SBMPSTO
      FROM DF_C_IMPUESTOS A
      JOIN DF_I_IMPUESTOS_SUBIMPUESTO B
        ON A.ID_IMPSTO = B.ID_IMPSTO
     WHERE A.CDGO_CLNTE = P_CDGO_CLNTE
       AND A.CDGO_IMPSTO = 'ICA'
       AND B.CDGO_IMPSTO_SBMPSTO = 'ICA';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      SITPR001('Error (1) en impuesto-subimpuesto: ' || SQLCODE || '-' ||
               SQLERRM,
               V_ARCHVO_ERROR);
    WHEN OTHERS THEN
      SITPR001('Error (2) en impuesto-subimpuesto: ' || SQLCODE || '-' ||
               SQLERRM,
               V_ARCHVO_ERROR);
  END;

  IF V_ID_IMPSTO IS NULL OR V_ID_IMPSTO_SBMPSTO IS NULL THEN

    O_MNSJE := 'Error en impuesto-subImpuesto, no existe en el sistema!';
    RETURN;
  END IF;

  V_ID_IMPSTO_ACTO := NULL;

  BEGIN
    SELECT IA.ID_IMPSTO_ACTO
      INTO V_ID_IMPSTO_ACTO
      FROM DF_I_IMPUESTOS_ACTO IA
     WHERE IA.ID_IMPSTO = V_ID_IMPSTO
       AND IA.ID_IMPSTO_SBMPSTO = V_ID_IMPSTO_SBMPSTO;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      SITPR001('Error (1) en impuesto acto: ' || SQLCODE || '-' || SQLERRM,
               V_ARCHVO_ERROR);
    WHEN OTHERS THEN
      SITPR001('Error (2) en impuesto acto: ' || SQLCODE || '-' || SQLERRM,
               V_ARCHVO_ERROR);
  END;

  IF V_ID_IMPSTO_ACTO IS NULL THEN

    O_MNSJE := 'Error en impuesto acto, no existe en el sistema!';
    RETURN;
  END IF;

  --CREAR EL REGISTRO DE CUENTA QUE NO EXISTE MONTERÍA
  BEGIN
    SELECT C.ID_BNCO_CNTA
      INTO V_ID_BNCO_CNTA
      FROM DF_C_BANCOS_CUENTA C
     WHERE C.ID_BNCO = 8
       AND C.NMRO_CNTA = '890888969';

  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      INSERT INTO DF_C_BANCOS_CUENTA
        (ID_BNCO, NMRO_CNTA, INDCDOR_CNTA_WS, DSCRPCION_CNTA)
      VALUES
        (8, '890888969', 'N', NULL);
  END;

  --CAMBIAR EL BANCO 55 POR 52 DAVIVIENDA MONTERÍA
  UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO A
     SET A.CDGO_BNCO = '52'
   WHERE A.CDGO_BNCO = '55';

  FOR R1 IN C1 LOOP
    SITPR001('Procesando vigencia: ' || R1.VGNCIA, V_ARCHVO);

    BEGIN
      SELECT A.ID_PRDO
        INTO V_ID_PRDO
        FROM DF_I_PERIODOS A
       WHERE A.CDGO_CLNTE = P_CDGO_CLNTE
         AND A.ID_IMPSTO = V_ID_IMPSTO
         AND A.ID_IMPSTO_SBMPSTO = V_ID_IMPSTO_SBMPSTO
         AND A.VGNCIA = R1.VGNCIA
         AND A.CDGO_PRDCDAD = 'ANU';
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        V_ID_PRDO := NULL;
    END;

    UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
       SET A.ID_PRDO = V_ID_PRDO
     WHERE A.VGNCIA = R1.VGNCIA
       AND A.ID_PRDO IS NULL;
  END LOOP;

  COMMIT;

  FOR R2 IN C2 LOOP
    SITPR001('Procesando vigencia: ' || R2.CDGO_CNCPTO, V_ARCHVO);

    BEGIN
      SELECT A.ID_CNCPTO, A.CTGRIA_CNCPTO
        INTO V_ID_CNCPTO, V_CTGRIA_CNCPTO
        FROM DF_I_CONCEPTOS A
       WHERE A.CDGO_CLNTE = P_CDGO_CLNTE
         AND A.ID_IMPSTO = V_ID_IMPSTO
         AND A.CDGO_CNCPTO = R2.CDGO_CNCPTO;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        V_ID_CNCPTO     := NULL;
        V_CTGRIA_CNCPTO := NULL;
    END;

    COMMIT;

    UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
       SET A.ID_CNCPTO       = V_ID_CNCPTO,
           A.TPO_CNCPTO      = V_CTGRIA_CNCPTO,
           A.ID_CNCPTO_CPTAL = DECODE(V_CTGRIA_CNCPTO,
                                      'I',
                                      NULL,
                                      V_ID_CNCPTO)
     WHERE A.CDGO_CNCPTO = R2.CDGO_CNCPTO
       AND A.ID_CNCPTO IS NULL;
  END LOOP;

  COMMIT;

  --SALDOS A FAVOR
  BEGIN
    SELECT C.ID_CNCPTO
      INTO V_ID_CNCPTO_SLDO_FVOR
      FROM DF_I_CONCEPTOS C
     WHERE C.ID_IMPSTO = V_ID_IMPSTO
       AND C.CDGO_CNCPTO = '999';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      insert into df_i_conceptos
        (CDGO_CLNTE,
         ID_IMPSTO,
         CDGO_CNCPTO,
         DSCRPCION,
         SNCION,
         UNCO,
         ACTVO,
         CDGO_CNCPTO_TPO,
         ID_CNCPTO_RLCNAL,
         CTGRIA_CNCPTO,
         INDCDOR_MGRDO)
      values
        (P_CDGO_CLNTE,
         V_ID_IMPSTO,
         '999',
         'SALDO A FAVOR MIGRADO',
         'N',
         'N',
         'N',
         'DBT',
         null,
         'C',
         null)
      RETURNING ID_CNCPTO INTO V_ID_CNCPTO_SLDO_FVOR;
      COMMIT;
  END;

  FOR R2_2 IN C2_2 LOOP
    UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
       SET A.ID_CNCPTO       = V_ID_CNCPTO_SLDO_FVOR,
           A.TPO_CNCPTO      = 'C',
           A.ID_CNCPTO_CPTAL = V_ID_CNCPTO_SLDO_FVOR
     WHERE A.CDGO_CNCPTO = R2_2.CDGO_CNCPTO
       AND A.ID_CNCPTO IS NULL;
  END LOOP;

  COMMIT;

  FOR R3 IN C3 LOOP
    BEGIN
      SELECT D.ID_CNCPTO
        INTO V_ID_CNCPTO_CPTAL
        FROM DF_I_IMPUESTOS_ACTO_CONCEPTO D
       WHERE D.ID_IMPSTO_ACTO = V_ID_IMPSTO_ACTO
         AND D.VGNCIA = R3.VGNCIA
         AND D.ID_PRDO = R3.ID_PRDO
         AND D.ID_CNCPTO_INTRES_MRA = R3.ID_CNCPTO;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        V_ID_CNCPTO_CPTAL := NULL;
    END;

    UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
       SET A.ID_CNCPTO_CPTAL = V_ID_CNCPTO_CPTAL
     WHERE A.VGNCIA = R3.VGNCIA
       AND A.ID_PRDO = R3.ID_PRDO
       AND A.ID_CNCPTO = R3.ID_CNCPTO
       AND A.TPO_CNCPTO = R3.TPO_CNCPTO
       AND A.ID_CNCPTO_CPTAL IS NULL;

  END LOOP;

  FOR R4 IN C4 LOOP

    V_ID_BNCO := NULL;

    BEGIN
      SELECT D.ID_BNCO
        INTO V_ID_BNCO
        FROM DF_C_BANCOS D
       WHERE D.CDGO_CLNTE = P_CDGO_CLNTE
         AND LPAD(D.CDGO_BNCO, 3, '0') = LPAD(R4.CDGO_BNCO, 3, '0');
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        V_ID_BNCO := NULL;
    END;

    IF V_ID_BNCO IS NOT NULL THEN

      UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO A
         SET A.ID_BNCO = V_ID_BNCO
       WHERE A.CDGO_BNCO = R4.CDGO_BNCO
         AND A.ID_BNCO IS NULL;

      BEGIN
        SELECT D.ID_BNCO_CNTA
          INTO V_ID_BNCO_CNTA
          FROM DF_C_BANCOS_CUENTA D
         WHERE D.ID_BNCO = V_ID_BNCO
           AND TRIM(D.NMRO_CNTA) = TRIM(R4.NMRO_CNTA);
      EXCEPTION
        WHEN NO_DATA_FOUND THEN
          V_ID_BNCO_CNTA := NULL;
      END;

      IF V_ID_BNCO_CNTA IS NOT NULL THEN

        UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO A
           SET A.ID_BNCO_CNTA = V_ID_BNCO_CNTA, A.ESTDO_RGSTRO = 'L'
         WHERE A.ID_BNCO = V_ID_BNCO
           AND TRIM(A.NMRO_CNTA) = TRIM(R4.NMRO_CNTA)
           AND A.ID_BNCO_CNTA IS NULL;
      ELSE
        UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO A
           SET A.ESTDO_RGSTRO = 'E',
               A.MNSJE_ERROR  = 'Error homologando cuenta'
         WHERE A.CDGO_BNCO = R4.CDGO_BNCO
           AND A.NMRO_CNTA = R4.NMRO_CNTA;
      END IF;
    ELSE
      UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO A
         SET A.ESTDO_RGSTRO = 'E',
             A.MNSJE_ERROR  = 'Error homologando banco'
       WHERE A.CDGO_BNCO = R4.CDGO_BNCO;
    END IF;

  END LOOP;

  COMMIT;

  FOR R5 IN C5 LOOP
    BEGIN
      SELECT C.ID_CNVNIO
        INTO V_ID_CNVNIO
        FROM GF_G_CONVENIOS C
       WHERE C.ID_SJTO_IMPSTO = R5.ID_SJTO_IMPSTO
         AND C.NMRO_CNVNIO = R5.NMRO_CNVNIO;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        V_ID_CNVNIO := NULL;
    END;

    IF V_ID_CNVNIO IS NOT NULL THEN
      UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO A
         SET A.ID_CNVNIO = V_ID_CNVNIO
       WHERE A.ID_INTRMDIA = R5.ID_INTRMDIA;
    END IF;

  END LOOP;

  COMMIT;

  UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO M
     SET M.ESTDO_RGSTRO = 'E',
         M.MNSJE_ERROR  = 'Error homologando conceptos en este recaudo'
   WHERE EXISTS (SELECT 1
            FROM MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_DETALLE A
           WHERE M.ID_INTRMDIA = A.ID_INTRMDIA_MSTRO
             AND (A.ID_PRDO IS NULL OR A.ID_CNCPTO IS NULL OR
                 A.ID_CNCPTO_CPTAL IS NULL));
  COMMIT;

  UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO M
     SET M.ESTDO_RGSTRO = 'E',
         M.MNSJE_ERROR  = 'Error homologando banco-cuenta'
   WHERE M.ID_BNCO_CNTA IS NULL;
  COMMIT;

  UPDATE MIGRA.MG_G_INTERMEDIA_ICA_RECAUDO_2025_MAESTRO M
     SET M.ESTDO_RGSTRO = 'E', M.MNSJE_ERROR = 'Sin Número de documento'
   WHERE M.Nmro_Dcmnto IS NULL;
  COMMIT;

END PRC_MIGRA_RECAUDOS_ICA_2025_2;
