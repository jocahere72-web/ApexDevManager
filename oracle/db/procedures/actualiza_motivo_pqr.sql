
  CREATE OR REPLACE EDITIONABLE PROCEDURE "ACTUALIZA_MOTIVO_PQR" (P_NMRO_RDCDO_DSPLAY IN VARCHAR,
                                                 P_ID_MTVO_NUEVO     IN NUMBER) IS

  CURSOR C1 IS(
    SELECT B.ID_INSTNCIA_FLJO_GNRDO_HJO,
           E.ID_INSTNCIA_TRNSCION,
           A.ID_INSTNCIA_FLJO,
           A.ID_SLCTUD,
           K.ID_SLCTUD_MTVO
      FROM PQ_G_SOLICITUDES A
      JOIN PQ_G_SOLICITUDES_MOTIVO K
        ON A.ID_SLCTUD = K.ID_SLCTUD
      JOIN PQ_D_MOTIVOS M
        ON K.ID_MTVO = M.ID_MTVO
      LEFT JOIN WF_G_INSTANCIAS_FLUJO_GNRDO B
        ON A.ID_INSTNCIA_FLJO = B.ID_INSTNCIA_FLJO
      JOIN WF_G_INSTANCIAS_FLUJO C
        ON B.ID_INSTNCIA_FLJO_GNRDO_HJO = C.ID_INSTNCIA_FLJO
      JOIN WF_D_FLUJOS D
        ON C.ID_FLJO = D.ID_FLJO
      JOIN WF_G_INSTANCIAS_TRANSICION E
        ON C.ID_INSTNCIA_FLJO = E.ID_INSTNCIA_FLJO
      JOIN WF_D_FLUJOS_TAREA F
        ON E.ID_FLJO_TREA_ORGEN = F.ID_FLJO_TREA
      JOIN WF_D_TAREAS G
        ON F.ID_TREA = G.ID_TREA
      JOIN SG_G_USUARIOS H
        ON E.ID_USRIO = H.ID_USRIO
      JOIN SI_C_TERCEROS I
        ON H.ID_TRCRO = I.ID_TRCRO
      JOIN WF_D_ESTADO_TRANSICION J
        ON E.ID_ESTDO_TRNSCION = J.ID_ESTDO_TRNSCION
     WHERE A.NMRO_RDCDO_DSPLAY = P_NMRO_RDCDO_DSPLAY
     GROUP BY B.ID_INSTNCIA_FLJO_GNRDO_HJO,
              E.ID_INSTNCIA_TRNSCION,
              A.ID_INSTNCIA_FLJO,
              A.ID_SLCTUD,
              K.ID_SLCTUD_MTVO);

  CURSOR C2 IS(
    SELECT B.ID_FLJO_TREA, A.ID_FLJO, A.ID_MTVO
      FROM PQ_D_MOTIVOS A
      JOIN WF_D_FLUJOS_TAREA B
        ON A.ID_FLJO = B.ID_FLJO
     WHERE A.ID_MTVO = P_ID_MTVO_NUEVO
       AND B.INDCDOR_INCIO = 'S');

BEGIN
  FOR R1 IN C1 LOOP
    FOR R2 IN C2 LOOP

      UPDATE PQ_G_SOLICITUDES_MOTIVO
         SET ID_MTVO = R2.ID_MTVO
       WHERE ID_SLCTUD_MTVO = R1.ID_SLCTUD_MTVO;
      COMMIT;

      UPDATE WF_G_INSTANCIAS_FLUJO
         SET ID_FLJO = R2.ID_FLJO
       WHERE ID_INSTNCIA_FLJO = R1.ID_INSTNCIA_FLJO_GNRDO_HJO;

      UPDATE WF_G_INSTANCIAS_TRANSICION
         SET ID_FLJO_TREA_ORGEN = R2.ID_FLJO_TREA
       WHERE ID_INSTNCIA_TRNSCION = R1.ID_INSTNCIA_TRNSCION;

    END LOOP;
    COMMIT ;
  END LOOP;

END;
