
  CREATE OR REPLACE EDITIONABLE FUNCTION "SITF_NIT_CC" (P_NMRO_IDNTFCCION VARCHAR2)
  return varchar2 is
  V_NMRO_IDNTFCCION NUMBER;
  V_ERROR           BOOLEAN;
  V_TPO_IDNTFCCION  VARCHAR2(1);
begin
  V_ERROR          := FALSE;
  V_TPO_IDNTFCCION := 'X';
  BEGIN
    V_NMRO_IDNTFCCION := TO_NUMBER(P_NMRO_IDNTFCCION);
  EXCEPTION
    WHEN OTHERS THEN
      V_ERROR := TRUE;
  END;

  IF NOT V_ERROR THEN
    IF V_NMRO_IDNTFCCION >= 800000000 AND V_NMRO_IDNTFCCION <= 999999999 THEN
      V_TPO_IDNTFCCION := 'N';
    ELSIF V_NMRO_IDNTFCCION >= 8000000000 THEN
      V_TPO_IDNTFCCION := 'N';
    ELSE
      V_TPO_IDNTFCCION := 'C';
    END IF;
  END IF;

  return(V_TPO_IDNTFCCION);
end SITF_NIT_CC;
/

