'WITH InstanciasOrdenadas AS (',
'    SELECT ',
'        a.nmro_rdcdo_dsplay,',
'        TRUNC(a.fcha_rdcdo) AS fecha_radicacion,',
'        a.id_instncia_fljo AS instncia_fljo_pqr,',
'        b.id_instncia_fljo_gnrdo_hjo AS instncia_fljo_hjo,',
'        d.dscrpcion_fljo AS MOTIVO,',
'        j.estdo_trnscion AS ESTADO,',
'        c.fcha_incio,',
'        i.prmer_nmbre ||'' ''|| i.prmer_aplldo AS funcionario,',
'        k.nmbre_slctnte AS solicitente,',
'        a.fcha_real,',
'        CASE ',
'            WHEN a.fcha_real IS NOT NULL THEN 0',
'            WHEN TRUNC(SYSDATE) >= TRUNC(CAST(a.fcha_pryctda AS DATE)) THEN 0',
'            WHEN TRUNC(SYSDATE) < TRUNC(CAST(a.fcha_pryctda AS DATE)) THEN TRUNC(ABS(CAST(a.fcha_pryctda AS DATE) - CAST(SYSDATE AS DATE)))',
'        END AS dias_faltantes,',
'        CASE ',
'            WHEN a.fcha_real IS NOT NULL THEN 0',
'            WHEN TRUNC(SYSDATE) > TRUNC(CAST(a.fcha_pryctda AS DATE)) THEN TRUNC(ABS(CAST(a.fcha_pryctda AS DATE) - CAST(SYSDATE AS DATE)))',
'            ELSE 0',
'        END AS dias_vencidos,',
'        CASE ',
'            WHEN TRUNC(SYSDATE) > TRUNC(CAST(a.fcha_pryctda AS DATE)) AND a.fcha_real IS NULL THEN 1',
'            ELSE 0',
'        END AS estdo,',
'        ROW_NUMBER() OVER (PARTITION BY a.nmro_rdcdo_dsplay ORDER BY e.ID_INSTNCIA_TRNSCION DESC) AS rn',
'    FROM ',
'        pq_g_solicitudes a',
'    LEFT JOIN ',
'        wf_g_instancias_flujo_gnrdo b ON a.id_instncia_fljo = b.id_instncia_fljo',
'    JOIN ',
'        wf_g_instancias_flujo c ON b.id_instncia_fljo_gnrdo_hjo = c.id_instncia_fljo',
'    JOIN ',
'        wf_d_flujos d ON c.id_fljo = d.id_fljo',
'    JOIN ',
'        wf_g_instancias_transicion e ON c.id_instncia_fljo = e.id_instncia_fljo',
'    JOIN ',
'        wf_d_flujos_tarea f ON e.id_fljo_trea_orgen = f.id_fljo_trea',
'    JOIN ',
'        wf_d_tareas g ON f.id_trea = g.id_trea',
'    JOIN ',
'        sg_g_usuarios h ON e.id_usrio = h.id_usrio',
'    JOIN ',
'        si_c_terceros i ON h.id_trcro = i.id_trcro',
'    JOIN ',
'        wf_d_estado_transicion j ON e.id_estdo_trnscion = j.id_estdo_trnscion',
'    JOIN ',
'        PQ_G_SOLICITUDES_MOTIVO p ON a.id_slctud=p.id_slctud',
'    JOIN ',
'        PQ_D_MOTIVOS q ON q.id_mtvo=p.id_mtvo',
'    JOIN ',
'        v_pq_g_solicitantes k ON a.id_slctud=k.id_slctud',
'    WHERE ',
'        a.fcha_rdcdo BETWEEN JSON_VALUE(:P37_JSON, ''$.fcha_incial'') AND JSON_VALUE(:P37_JSON, ''$.fcha_fnal'')',
'        --AND a.nmro_rdcdo_dsplay=''2022-10008491''',
')',
'SELECT ',
'    nmro_rdcdo_dsplay,',
'    fecha_radicacion,',
'    instncia_fljo_pqr,',
'    instncia_fljo_hjo,',
'    MOTIVO,',
'    ESTADO,',
'    fcha_incio,',
'    funcionario,',
'    solicitente,',
'    fcha_real,',
'    dias_faltantes,',
'    dias_vencidos,',
'    estdo',
'FROM ',
'    InstanciasOrdenadas',
'WHERE ',
'    rn = 1;'))
