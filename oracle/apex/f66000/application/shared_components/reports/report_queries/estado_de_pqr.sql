'            then 0',
'            when trunc(sysdate) > trunc(cast (a.fcha_pryctda as date))',
'            then trunc(abs( cast(a.fcha_pryctda as date) - cast(sysdate as date) ))',
'            else 0',
'       end dias_vencidos,',
'       case when trunc(sysdate) > trunc(cast (a.fcha_pryctda as date)) and  a.fcha_real is null',
'            then 1',
'            else 0',
'            end estdo',
'from pq_g_solicitudes a',
